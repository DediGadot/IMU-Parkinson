"""T1 GLASS-CEILING PUSH — SLOT A.2 (codex-recommended variant of slot A)
Stage-3 stacked-correction with shrunken meta-stacker on per-item basis functions.

Codex (2026-05-15T08:08Z) argued that K=30 raw step-function features on T1_sum residual
is still high-df at N=92. The better design uses the validated per-item correction
predictions for items 10/13/14 as basis functions; train a low-df shrunken meta-stacker
(Ridge or NNLS) on the outer T1_sum residual using those 3 basis predictions.

Per outer LOOCV fold:
  1) For each item in {10, 13, 14}: produce inner-LOOCV correction predictions on the
     outer train set (91 subjects -> 91 inner-OOF corrections). Plus the outer-test
     correction predicted by training on all 91.
  2) Build B_train (91 x 3) and B_test (1 x 3) of corrections.
  3) Inner 5-fold CV on B_train -> y_residual_T1_sum picks the best meta-stacker:
       Ridge alpha in {0.01, 0.1, 1.0, 10.0} or NNLS.
  4) Refit on full B_train, predict outer-test correction.
  5) yhat_corrected[i] = yhat_iter34[i] + correction.

Inner-LOOCV correction predictions for items 10/13/14 ensure the outer-train basis
matrix is leak-clean: the prediction for outer-train subject j was generated by a
model that did NOT see j in its training. The outer-test correction was generated
by a model that did NOT see outer-test in its training (outer fold guarantee).

Family-specific features:
  item 10 -> MFDFA family
  item 13 -> PH family
  item 14 -> PH family

Compute: ~91 outer folds * 91 inner folds * 3 items * 1 Ridge fit each ~= 25k Ridge fits.
Plus 5-fold inner CV on meta. Expected wall-clock 5-10 min on master single-core.

5-null gate identifiers per project rule:
  Null 1 scrambled-y: permute y_t1_sum at outer scope.
  Null 2 sid-shuffle: permute SF cache SIDs.
  Null 3 canary-noise: additive gaussian on SF features sigma=0.01.
  Null 4 library-exclusion: firewall_check.py exit 0.
  Null 5 inductive-vs-transductive: cohort-stat normalization variant.
"""
